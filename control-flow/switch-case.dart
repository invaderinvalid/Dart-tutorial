void main(){
  //switch case statement:applicable for int and 'String'

  String grade ="c";

  switch (grade){
    case 'A':
    print("Excellent grade");
    break;

    case 'B':
    print("Very good");
    break;

    case 'c':
    print("good enoughh");
    break;

    case 'd':
    print("work hard");
    break;

    case 'f':
    print("you have failed!");
    break;

    default:
    print("invalid input");
  }


}